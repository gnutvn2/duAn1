    public void loadDataToTable() {
        List<QuanLyHoaDon> list = service.getAllHoaDon();
        model.setRowCount(0);
        for (QuanLyHoaDon ql : list) {
            model.addRow(new Object[]{ql.getMaHoaDon(), ql.getNhanVien(), ql.getKhachHang(),
                ql.getThoiGian(), ql.getThanhTien(), ql.isTrangThai() == true ? "Đã thanh toán" : "Chưa thanh toán"});
        }
        // Lấy số lượng bản ghi hiện có trong bảng tblHoaDon
        int rowCount = tblQLHOADON.getRowCount();
        // Hiển thị số lượng bản ghi lên JLabel
        lblSoLuong.setText("" + rowCount);
    }

    


    private void tblQLHOADONMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblQLHOADONMouseClicked
        // TODO add your handling code here:
        // Lấy chỉ mục của dòng được chọn
        int selectedRow = tblQLHOADON.getSelectedRow();
//         showHoaDon();

// Lấy mã hóa đơn từ dòng được chọn
        int selectedMaHoaDon = (int) tblQLHOADON.getValueAt(selectedRow, 0); // 0 là chỉ mục cột chứa mã hóa đơn
        showHoaDonChiTiet(selectedMaHoaDon);

// Tính tổng tiền từ các dòng có cùng mã hóa đơn
        double tongTien = 0.0;

        for (int i = 0; i < tblQLHOADON.getRowCount(); i++) {
            int maHoaDon = (int) tblQLHOADON.getValueAt(i, 0); // Lấy mã hóa đơn từ dòng i
            if (maHoaDon == selectedMaHoaDon) {
                double thanhTien = Double.parseDouble(tblQLHOADON.getValueAt(i, 4).toString()); // columnIndexTotal là chỉ mục cột chứa số tiền
                tongTien += thanhTien;
            }
        }
        
       
// Hiển thị tổng tiền lên Label
        lblTongTienHoaDon.setText(String.valueOf(tongTien));
        
        

    }//GEN-LAST:event_tblQLHOADONMouseClicked

